 /* MS_TRAININGS */
 -- MS_TRAININGS.ID AS `MS_TRAININGS_ID`,
 MS_TRAININGS.NAME AS `TRAINING_NAME`,
 /* END OF : MS_TRAININGS */

 /* MS_AREA_LEVELS */
 -- MS_AREA_LEVELS.ID AS `MS_AREA_LEVELS_ID`,
 MS_AREA_LEVELS.NAME AS `TRAINING_AREA_LEVEL_NAME`,
 MS_AREA_LEVELS.TYPE AS `TRAINING_AREA_LEVEL_TYPE`,
 /* END OF : MS_AREA_LEVELS */

 /* MS_BENEFICIARIES */
 -- MS_BENEFICIARIES.ID AS `MS_BENEFICIARIES_ID`,
 MS_BENEFICIARIES.NAME AS `TRAINING_BENEFICIARIES_NAME`,
 /* END OF : MS_BENEFICIARIES */

 /* MS_FUNDING_SOURCES */
 -- MS_FUNDING_SOURCES.ID AS `MS_FUNDING_SOURCES_ID`,
 MS_FUNDING_SOURCES.CITY_ID AS `TRAINING_FUNDING_SOURCE_CITY_ID`,
 -- CITYFS.NAME AS `TRAINING_FUNDING_SOURCE_CITY_NAME`,
 MS_FUNDING_SOURCES.PROVINCE_ID AS `TRAINING_FUNDING_SOURCE_PROVINCE_ID`,
 -- PROVINCEFS.NAME AS `TRAINING_FUNDING_SOURCE_PROVINCE_NAME`,
 MS_FUNDING_SOURCES.COUNTRY_ID AS `TRAINING_FUNDING_SOURCE_COUNTRY_ID`,
 -- COUNTRYFS.NAME AS `TRAINING_FUNDING_SOURCE_COUNTRY_NAME`,
 MS_FUNDING_SOURCES.NAME AS `TRAINING_FUNDING_SOURCE_NAME`,
 MS_FUNDING_SOURCES.PHONE_NO1 AS `TRAINING_FUNDING_SOURCE_PHONE_NO1`,
 MS_FUNDING_SOURCES.PHONE_NO2 AS `TRAINING_FUNDING_SOURCE_PHONE_NO2`,
 MS_FUNDING_SOURCES.EMAIL1 AS `TRAINING_FUNDING_SOURCE_EMAIL1`,
 MS_FUNDING_SOURCES.EMAIL2 AS `TRAINING_FUNDING_SOURCE_EMAIL2`,
 MS_FUNDING_SOURCES.WEBSITE AS `TRAINNG_FUNDING_SOURCE_WEBSITE`,
 MS_FUNDING_SOURCES.ADDRESS AS `TRAINING_FUNDING_SOURCE_ADDRESS`,
 /* END OF : MS_FUNDING_SOURCES */

 /* MS_VENUE */
 -- MS_VENUES.ID AS `TRAINING_VENUE_ID`,
 MS_VENUES.CITY_ID AS `TRAINING_VENUE_CITY_ID`,
 -- CITYVE.NAME AS `TRAINING_VENUE_CITY_NAME`,
 MS_VENUES.PROVINCE_ID AS `TRAINING_VENUE_PROVINCE_ID`,
 -- PROVINCEVE.NAME AS `TRAINING_VENUE_PROVINCE_NAME`,
