 -- CITYOR.NAME AS `TRAINING_ORGANIZATION_CITY_NAME`,
 MS_ORGANIZATIONS.PROVINCE_ID AS `TRAINING_ORGANIZATION_PROVINCE_ID`,
 -- PROVINCEOR.NAME AS `TRAINING_ORGANIZATION_PROVINCE_NAME`,
