 MS_TRAINERS.PHONE_NO AS `TRAINER_PHONE_NO`,
 MS_TRAINERS.EMAIL1 AS `TRAINER_EMAIL1`,
 MS_TRAINERS.EMAIL2 AS `TRAINER_EMAIL2`,
 MS_TRAINERS.FB AS `TRAINER_FB`,
 MS_TRAINERS.TWITTER AS `TRAINER_TWITTER`,
 /* END OF : MS_TRAINERS */

 /* MS_ROLES */
 -- MS_ROLES.ID AS `TRAINER_ROLE_ID`
 MS_ROLES.NAME AS `ROLE_NAME`
 /* END OF : MS_ROLES */
