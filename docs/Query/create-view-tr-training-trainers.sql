 TR_TRAININGS.VENUE_ID AS `TRAINING_VENUE_ID`,
 TR_TRAININGS.ORGANIZATION_ID AS `TRAINING_ORGANIZATION_ID`,
 TR_TRAININGS.SDATE AS `TRAINING_SDATE`,
