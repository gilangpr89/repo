 /* END OF : MS_ORGANIZATIONS */
 /* END OF : TR_TRAININGS */

 /* MS_CITY */
 -- MS_CITY.ID AS `TRAINER_CITY_ID`
 CITYTR.NAME AS `CITY_NAME`,
 /* END OF : MS_CITY */

 /* MS_PROVINCE */
