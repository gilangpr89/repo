 MS_VENUES.PHONE_NO2 AS `TRAINING_VENUE_PHONE_NO2`,
 MS_VENUES.EMAIL1 AS `TRAINING_VENUE_EMAIL1`,
 MS_VENUES.EMAIL2 AS `TRAINING_VENUE_EMAIL2`,
