 TR_TRAINING_TRAINERS
 JOIN
 TR_TRAININGS ON TR_TRAINING_TRAINERS.TRAINING_ID = TR_TRAININGS.ID
 JOIN
 USERS ON TR_TRAININGS.USER_ID = USERS.USER_ID
 JOIN
 MS_TRAININGS ON TR_TRAININGS.TRAINING_ID = MS_TRAININGS.ID
 JOIN
 MS_AREA_LEVELS ON TR_TRAININGS.AREA_LEVEL_ID = MS_AREA_LEVELS.ID
 JOIN
 MS_BENEFICIARIES ON TR_TRAININGS.BENEFICIARIES_ID = MS_BENEFICIARIES.ID
 JOIN
 MS_FUNDING_SOURCES ON TR_TRAININGS.FUNDING_SOURCE_ID = MS_FUNDING_SOURCES.ID 
 	-- JOIN MS_CITY CITYFS ON MS_FUNDING_SOURCES.CITY_ID = CITYFS.ID
 	-- JOIN MS_PROVINCE PROVINCEFS ON MS_FUNDING_SOURCES.PROVINCE_ID = PROVINCEFS.ID
 	-- JOIN MS_COUNTRY COUNTRYFS ON MS_FUNDING_SOURCES.COUNTRY_ID = COUNTRYFS.ID
 JOIN
 MS_VENUES ON TR_TRAININGS.VENUE_ID = MS_VENUES.ID
 	-- JOIN MS_CITY CITYVE ON MS_VENUES.CITY_ID = CITYVE.ID
 	-- JOIN MS_PROVINCE PROVINCEVE ON MS_VENUES.PROVINCE_ID = PROVINCEVE.ID
 	-- JOIN MS_COUNTRY COUNTRYVE ON MS_VENUES.COUNTRY_ID = COUNTRYVE.ID
 JOIN
 MS_ORGANIZATIONS ON TR_TRAININGS.ORGANIZATION_ID = MS_ORGANIZATIONS.ID
 	-- JOIN MS_CITY CITYOR ON MS_ORGANIZATIONS.CITY_ID = CITYOR.ID
 	-- JOIN MS_PROVINCE PROVINCEOR ON MS_ORGANIZATIONS.PROVINCE_ID = PROVINCEOR.ID
 	-- JOIN MS_COUNTRY COUNTRYOR ON MS_ORGANIZATIONS.COUNTRY_ID = COUNTRYOR.ID
 JOIN
 MS_CITY CITYTR ON TR_TRAINING_TRAINERS.CITY_ID = CITYTR.ID
 JOIN
 MS_PROVINCE PROVINCETR ON TR_TRAINING_TRAINERS.PROVINCE_ID = PROVINCETR.ID
 JOIN
 MS_COUNTRY COUNTRYTR ON TR_TRAINING_TRAINERS.COUNTRY_ID = COUNTRYTR.ID
 JOIN
 MS_TRAINERS ON TR_TRAINING_TRAINERS.TRAINER_ID = MS_TRAINERS.ID
 JOIN
 MS_ROLES ON TR_TRAINING_TRAINERS.ROLE_ID = MS_ROLES.ID