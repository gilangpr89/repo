 /* END OF : MS_COUNTRY */

 /* MS_TRAINERS */
 -- MS_TRAINERS.ID AS `TRAINER_ID`,
 MS_TRAINERS.NAME AS `TRAINER_NAME`,
 MS_TRAINERS.NICKNAME AS `TRAINER_NICKNAME`,
 MS_TRAINERS.GENDER AS `TRAINER_GENDER`,
